package Day3_100822;

public class T1_If_else_Condition {

    public static void main(String[] args) {
        // declaring variable for if else condition
        int a, b;
        a = 2;
        b = 3;
        //verify is a is less than b or not.
        if (a < b) {
            System.out.println("a is less than b");
        } else {
            System.out.println("a is either grater than or equal to b");
        }// end of if_else condition

    }// end of main

}// end of java class
